package sample;

class Planets {

    // Fields
    private String name;
    private double mass;
    private int diameter;
    private int density;
    private double gravity;

    //constructor
    Planets(String theName, double theMass, int theDiameter, int theDensity, double theGravity) {
        name = theName;
        mass = theMass;
        diameter = theDiameter;
        density = theDensity;
        gravity = theGravity;
    }
}
